private void cadastrarHospedes() {
    // Capturando os dados do textField
    String nome = txtNome.getText();
    String cpf = txtCpf.getText().trim();
    String telefone = txtTelefone.getText().trim();
    String email = txtEmail.getText().trim();
    String sexo = txtSexo.getText().trim();
    String dataNascimento = txtDataNascimento.getText().trim();

    if (cpf.isEmpty() || telefone.isEmpty() || nome.isEmpty()) {
        JOptionPane.showMessageDialog(null, "‚ùå Preencha todos os campos obrigat√≥rios!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        // Criando o objeto HospedeDTO
        HospedesDTO novoHospede = new HospedesDTO(nome, cpf, dataNascimento, telefone, email, sexo);
        
        System.out.println("Dados capturados do formulario: " + novoHospede.toString());

        // Query SQL para inserir o h√≥spede
        String query = "INSERT INTO hospedes (nome, cpf, telefone, email, sexo, dataNascimento) VALUES (?, ?, ?, ?, ?, ?)";

        // Executa o comando SQL usando o m√©todo da classe Conexao
        boolean sucesso = Conexao.executarComandoSQL(query, nome, cpf, telefone, email, sexo, dataNascimento);

        if (sucesso) {
            JOptionPane.showMessageDialog(null, "H√≥spede cadastrado com sucesso.");
            listarHospedes(); // Atualiza a tabela
            limparCamposHospede();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar h√≥spede.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao cadastrar h√≥spede: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
}

private void atualizarHospede() throws SQLException {
    int selectedRow = tblHospedes.getSelectedRow(); // üîπ Obt√©m a linha selecionada
    System.out.println("üìå Tentando atualizar... Linha selecionada: " + selectedRow);

    if (selectedRow == -1) {  // Se nenhuma linha estiver selecionada, exibe erro
        JOptionPane.showMessageDialog(null, "‚ùå Nenhum h√≥spede foi selecionado!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obt√©m o CPF do h√≥spede selecionado
    String cpfHospedeSelecionado = (String) tblHospedes.getValueAt(selectedRow, 1); // Coluna 1 = CPF
    System.out.println("üìå CPF do h√≥spede selecionado: " + cpfHospedeSelecionado);

    if (cpfHospedeSelecionado.isEmpty()) {
        JOptionPane.showMessageDialog(null, "CPF do h√≥spede inv√°lido.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obt√©m os dados dos campos
    String nome = txtNome.getText();
    String cpf = txtCpf.getText();
    String telefone = txtTelefone.getText();
    String email = txtEmail.getText();
    String sexo = txtSexo.getText();
    String dataNascimento = txtDataNascimento.getText();

    if (nome.isEmpty() || cpf.isEmpty() || telefone.isEmpty() || email.isEmpty() || sexo.isEmpty() || dataNascimento.isEmpty()) {
        JOptionPane.showMessageDialog(null, "‚ùå Preencha todos os campos!", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // SQL para atualizar o h√≥spede
    String query = "UPDATE hospedes SET nome = ?, cpf = ?, telefone = ?, email = ?, sexo = ?, dataNascimento = ? WHERE cpf = ?";

    boolean sucesso = Conexao.executarComandoSQL(query, nome, cpf, telefone, email, sexo, dataNascimento, cpfHospedeSelecionado);

    if (sucesso) {
        JOptionPane.showMessageDialog(null, "‚úÖ H√≥spede atualizado com sucesso.");
        listarHospedes();  // Atualiza a tabela
    } else {
        JOptionPane.showMessageDialog(null, "‚ùå Erro ao atualizar o h√≥spede.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
}

private void excluirHospede() {
    int selectedRow = tblHospedes.getSelectedRow(); // Obt√©m a linha selecionada

    if (selectedRow == -1) {  // Verifica se um h√≥spede foi selecionado
        JOptionPane.showMessageDialog(null, "Selecione um h√≥spede para excluir.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obt√©m o CPF do h√≥spede selecionado
    String cpfHospedeSelecionado = (String) tblHospedes.getValueAt(selectedRow, 1); // Coluna 1 = CPF
    System.out.println("CPF do h√≥spede a excluir: " + cpfHospedeSelecionado);  // üî• Debug

    String query = "DELETE FROM hospedes WHERE cpf = ?";

    // Chama o m√©todo do ConexaoDAO para executar a exclus√£o
    boolean sucesso = Conexao.executarComandoSQL(query, cpfHospedeSelecionado);

    if (sucesso) {
        JOptionPane.showMessageDialog(null, "H√≥spede exclu√≠do com sucesso.");
        listarHospedes();  // Atualiza a lista de h√≥spedes
    } else {
        JOptionPane.showMessageDialog(null, "Erro ao excluir o h√≥spede.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
}

private void listarHospedes() {
    try {
        String query = "SELECT * FROM hospedes";
        
        // Usando a classe Conexao para obter a conex√£o
        try (Connection conn = new Conexao().getConnection(); // Obt√©m a conex√£o
             PreparedStatement pst = conn.prepareStatement(query);
             ResultSet rs = pst.executeQuery()) {

            DefaultTableModel model = (DefaultTableModel) tblHospedes.getModel();
            model.setNumRows(0); // Limpa a tabela antes de adicionar novos dados

            while (rs.next()) {
                Object[] rowData = {
                    rs.getString("nome"),
                    rs.getString("cpf"),
                    rs.getString("dataNascimento"),
                    rs.getString("telefone"),
                    rs.getString("email"),
                    rs.getString("sexo")
                };
                model.addRow(rowData);
            }
            tblHospedes.repaint(); // Atualiza a tabela
            tblHospedes.revalidate();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Erro ao carregar os h√≥spedes: " + e.getMessage());
    }
}

